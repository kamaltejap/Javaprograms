public class LoopDoWhile {
    /* 'do' is used for,if We want execute a block even if our condition is false,
     at least once we have to execute
     
     */
     public static void main(String[] args){
        
    int i=1;

    do{
        System.out.println("Hi"+i);
        i++;
    }
    while(i<=5);
    {
     System.out.println("hello"+i);  
    }
}
}
